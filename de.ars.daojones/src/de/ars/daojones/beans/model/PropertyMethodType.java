package de.ars.daojones.beans.model;

/**
 * The types of methods that a property method can be.
 * 
 * @author <a href="mailto:ralf.zahn@ars.de">Ralf Zahn</a>
 */
public enum PropertyMethodType {

  /**
   * Indicates that the method is a getter.
   */
  GETTER,
  /**
   * Indicates that the method is a setter.
   */
  SETTER;

}
