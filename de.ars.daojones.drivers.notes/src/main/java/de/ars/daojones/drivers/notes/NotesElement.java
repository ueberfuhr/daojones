package de.ars.daojones.drivers.notes;

/**
 * An enumeration of Notes elements that can be referred to.
 * 
 * @author Ralf Zahn, ARS Computer und Consulting GmbH, 2013
 * @since 2.0
 */
public enum NotesElement {

  /**
   * A single document.
   */
  DOCUMENT,
  /**
   * A view.
   */
  VIEW,
  /**
   * A view entry.
   */
  VIEW_ENTRY,
  /**
   * A database.
   */
  DATABASE;

}
